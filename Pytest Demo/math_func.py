
def add(x: int, y: int) -> int:
    return x + y


def multiply(x: int, y: int) -> int:
    return x * y


def divide(x: int, y: int) -> float:
    return x / y


def add_string(x: str, y: str) -> str:
    return x + y


def multiply_string(x: str, y: int) -> str:
    return x * y


def add_any_type(arg1, arg2):
    return arg1 + arg2
